import java.util.Scanner;

public class ExampleTwo {
    public static void main(String[] args) {
        //Take 2 variables 'a' & 'b' and print their sum.
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int sum = a + b;
        System.out.println(sum);
    }
}
